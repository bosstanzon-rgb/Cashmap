export const translate = (key: string, languageCode?: string) => {
  const short = (languageCode ?? "en").toLowerCase().split("-")[0];
  return STRINGS[short]?.[key] ?? STRINGS.en[key] ?? key;
};

export const t = (
  key: string,
  languageCode?: string,
  vars?: Record<string, string | number>
) => {
  const template = translate(key, languageCode);
  if (!vars) return template;
  return Object.entries(vars).reduce(
    (acc, [k, v]) => acc.replaceAll(`{${k}}`, String(v)),
      template
  );
};

/** For background tasks and services that cannot use the Zustand store. */
export const getPersistedLanguageCode = async (): Promise<string> => {
  const raw = await AsyncStorage.getItem(LANGUAGE_CODE_KEY);
  const v = raw?.trim().toLowerCase();
  return v || "en";
};
